
#Equal to
num1 = 10
num2 = num1
print(num1,num2)

#+= | num1 += num2 i.e num1 = num2 - num1
num1 = 2
num1 += num2
print(num1,num2)

#-= | num1 -= num2 i.e num1 = num2 - num1
num1 = 5
num1 -= num2
print(num1,num2)

#*= | num1 *= num2 i.e num1 = num2 * num1
num1 = 10
num1 *= num2
print(num1,num2)

#/= | num1 /= num2 i.e num1 = num2 / num1
num1 = 10
num1 /= num2
print(num1,num2)